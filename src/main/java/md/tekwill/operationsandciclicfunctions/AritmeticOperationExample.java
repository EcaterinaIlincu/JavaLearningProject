package md.tekwill.operationsandciclicfunctions;

public class AritmeticOperationExample {
    public static void main(String[] args) {
        int a = 33;
        int c = 4;
      //suma
        int sum = a + c;
        System.out.println("Suma: " + sum);
        System.out.println("Suma: " + (a + c));

        //substraction
        int sub = a - c;
        System.out.println("Rezultatul: " + sub);

        //multiplication
        int result = a * c;
        System.out.println("Rezultatul: " + result);

        //Devision
        result = a / c;
        System.out.println("Rezultatul impartirii: " + result);

        //Devision with remainder
        result = a % c;
        System.out.println("Rezultatul este: " + result);


    }
}
